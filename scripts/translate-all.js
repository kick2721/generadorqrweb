let currentCode = null;
let blockLines = [];
const langCodes = Object.keys(translations);

function flushBlock() {
  if (currentCode && blockLines.length > 0) {
    const vals = translations[currentCode];
    if (vals) {
      const insertLines = Object.keys(vals).map(k => `    ${k}: ${JSON.stringify(vals[k])},`);
      let perMonthIdx = -1;
      for (let j = blockLines.length - 1; j >= 0; j--) {
        if (blockLines[j].trim().startsWith("perMonth:")) {
          perMonthIdx = j;
          break;
        }
      }
      if (perMonthIdx >= 0) {
        blockLines.splice(perMonthIdx + 1, 0, ...insertLines);
      } else {
        blockLines.splice(blockLines.length - 1, 0, ...insertLines);
      }
    }
    out.push(...blockLines);
    blockLines = [];
  }
}

for (let i = 0; i < lines.length; i++) {
  const line = lines[i];
  const trimmed = line.trim();
  const blockStart = trimmed.match(/^(?:"?([a-z]{2}(?:-[A-Z]{2})?)"?):\s*\{$/);
  if (blockStart && langCodes.includes(blockStart[1])) {
    flushBlock();
    currentCode = blockStart[1];
    blockLines.push(line);
    continue;
  }
  if (currentCode) {
    blockLines.push(line);
    if (trimmed === "}," || trimmed === "}") {
      flushBlock();
      currentCode = null;
    }
    continue;
  }
  flushBlock();
  out.push(line);
}
flushBlock();

